public class Aula01 {

    public static void main(String[] args) {

        //concatenação simples
        String palavra = "tele";
        String palavraI = "fone";
        String palavraII = "passa";
        String palavraIII = "tempo";

        System.out.println(palavra + palavraI);
        System.out.println(palavraII + palavraIII);

        //concatenação composta
        String parte = "So";
        String parteI = "l";
        String parteII = "u";
        String parteIII = "ç";
        String parteIV = "ã";
        String parteV = "o";

        System.out.println(parte + parteI + parteII + parteIII + parteIV + parteV);

        //concatenação println
        System.out.println("Concatenação:\n\n"
                + "Este é um exemplo prático de concatenação de string. \n"
                + "Aqui, a string da linha de cima é "
                + "sempre concatenada com a string da linha de baixo");


    }
}
